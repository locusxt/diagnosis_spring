package com.locusxt.app.domain;

public class ZNodes {
	String id;
	String pId;
	String name;
	
	public void setId(String id){
		this.id = id;
	}
	
	public String getId(){
		return this.id;
	}
	
	public void setPId(String pId){
		this.pId = pId;
	}
	
	public String getPId(){
		return this.pId;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return this.name;
	}
}
