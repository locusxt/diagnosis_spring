package com.locusxt.app.domain;

public class ZNodes {
	String id;
	String pid;
	String name;
	
	public void setId(String id){
		this.id = id;
	}
	
	public String getId(){
		return this.id;
	}
	
	public void setPId(String pid){
		this.pid = pid;
	}
	
	public String getPId(){
		return this.pid;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return this.name;
	}
}
